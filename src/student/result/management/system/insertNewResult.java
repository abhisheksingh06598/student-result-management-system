    private void jButton6ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton6ActionPerformed
        String rollNumber = jTextField1.getText();
        String physicsMarks = jTextField2.getText();
         String chemistryMarks  = jTextField3.getText();
          String mathsMarks  = jTextField4.getText();
           String  dbmsMarks = jTextField5.getText();
            String osMarks = jTextField6.getText();
            
            try{
            Class.forName("com.mysql.jdbc.Driver");
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/srm","root","rootpassword");
            Statement st = con.createStatement();
            
            ResultSet rs = st.executeQuery("select* from student where rollNumber = '"+rollNumber+"'");
            
            if(rs.next()){
                st.executeUpdate("insert into result(rollNumber, physicsMarks, chemistryMarks, mathsMarks, dbmsMarks, osMarks) values('"+rollNumber+"', '"+physicsMarks+"', '"+chemistryMarks+"', '"+mathsMarks+"', '"+dbmsMarks+"', '"+osMarks+"')");
                JOptionPane.showMessageDialog(null,"Successfully Updated the Result");
                setVisible(false);
                
                new insertNewResult().setVisible(true);
            }
            
            else{
            JOptionPane.showMessageDialog(null,"Roll Number does Not exsist");
            }
            }
            
            catch(Exception e){
                JOptionPane.showMessageDialog(null,e.toString());
            }
       
    }//GEN-LAST:event_jButton6ActionPerformed

